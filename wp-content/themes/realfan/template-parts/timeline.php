            <li class="selected" data-date="16/01/2014">
                <h2>2700 a.C. - Primeiro registro</h2>
                <p>
                    A primeira farmacopeia (conjunto de informações técnicas sobre produtos farmacêuticos) do
                    mundo, chamada Pen Ts’ao Ching, recomenda o uso da cannabis para dores nas articulações,
                    prisão de ventre e malária.
                </p>
            </li>

            <li data-date="28/02/2014">
                <h2>1500 a.C - No Egito dos Faraós</h2>
                <p>
                    No Antigo Egito, o livro de medicina conhecido como Papyrus Ebers (um dos tratados médicos
                    mais antigos e importantes que se conhece) indica a cannabis para cólicas menstruais e
                    inflamações nos olhos.
                </p>
            </li>

            <li data-date="20/04/2014">
                <h2>50 d.C - No principal livro de medicina</h2>
                <p>
                    No Materia Medica, principal livro de medicina até o século 15, o médico grego Pedanius
                    Dioscórides cita a cannabis como remédio para problemas de articulação e inflamações.</p>
            </li>

            <li data-date="30/06/2014">
                <h2>1840 - Nos medicamentos dos EUA</h2>
                <p>Os primeiros remédios a base de cannabis surgem nos EUA.</p>
            </li>

            <li data-date="09/07/2014">
                <h2>1841 - Na Europa</h2>
                <p>
                    Os textos do médico inglês William O'Shaughnessy popularizam o uso medicinal da cannabis
                    na Europa. Após uma temporada na Índia, o médico publica artigos sobre o uso de planta para
                    tratar dores nas articulações, espasmos musculares e epilepsia em crianças.</p>
            </li>

            <li data-date="30/08/2014">
                <h2>1889 - Na prestigiada revista científica</h2>
                <p>
                    O The Lancet, revista científica ainda em atividade, divulga estudo sobre o tratamento da
                    dependência de ópio com cannabis.</p>
            </li>

            <li data-date="15/09/2014">
                <h2>1893 - Nos estudos pelo mundo</h2>
                <p>Estudo britânico na Índia conclui que a cannabis é benéfica para insônia, diabetes e ansiedade.</p>
            </li>

            <li data-date="10/12/2014">
                <h2>1905 - No Brasil de 1900</h2>
                <p>
                    Os Grimault “Cigarros Índios”, feitos de cannabis, são vendidos no Brasil para tratar insônia e asma. Enquanto isso, os extratos da planta são vendidos em diversos países da Europa e da América como analgésicos e antiespasmódicos.</p>
            </li>

            <li data-date="19/01/2015">
                <h2>1937 - A proibição</h2>
                <p>
                    A proibição da cannabis nos EUA impede sua prescrição. Nos anos seguintes, o mesmo acontece no restante do mundo.</p>
            </li>

            <li data-date="03/03/2015">
                <h2>1980 - Nos estudos brasileiros</h2>
                <p>
                    Estudo brasileiro testa a eficácia do canabidiol, derivado da cannabis, para o tratamento de epilepsia. Entre os oito pacientes testados, sete melhoram e quatro não tiveram mais crises.</p>
            </li>

            <li data-date="04/04/2015">
                <h2>1990 - Na holanda</h2>
                <p>
                    A empresa HortaPharm é criada na Holanda para o desenvolvimento de variedades de cannabis destinadas ao uso medicinal.</p>
            </li>

            <li data-date="03/05/2015">
                <h2>1996 - Reconsiderando a proibição</h2>
                <p>
                    A “lei da compaixão” é aprovada por plebiscito na Califórnia e o estado americano torna-se o primeiro a aprovar o uso medicinal da cannabis.</p>
            </li>

            <li data-date="03/06/2015">
                <h2>1998 - No desenvolvimento de remédios</h2>
                <p>
                    No Reino Unido a empresa GW Pharmaceuticals é fundada para criar remédios à base de cannabis, como o Sativex® e o Epidiolex®.</p>
            </li>

            <li data-date="03/07/2015">
                <h2>2001 - No programa federal de saúde</h2>
                <p>
                    O Canadá, por ordem judicial, torna-se o primeiro país do mundo a ter um programa federal que fornece cannabis com fins terapêuticos.
                </p>
            </li>

            <li data-date="03/08/2015">
                <h2>04/2014 - A saúde vence a proibição</h2>
                <p>
                    No Brasil, a família de uma criança de 5 anos portadora de uma síndrome rara que causa convulsões, torna-se a primeira a ter autorização judicial para usar um produto de cannabis. Após a popularização do filme “Ilegal” - que conta a história da garota, a ANVISA passa a receber mais pedidos de autorização de produtos à base de CBD – poderoso anti convulsivo extraído da cannabis.</p>
            </li>

            <li data-date="03/09/2015">
                <h2>12/2014 - Prescrição autorizada</h2>
                <p>
                    Conselho Federal de Medicina autoriza médicos a prescreverem o canabidiol (CBD) para crianças com epilepsia que não tenham respondido a outros tratamentos.
                </p>
            </li>

            <li data-date="03/10/2015">
                <h2>2015 - CBD não é mais proibido</h2>
                <p>
                    Anvisa retira canabidiol (CBD) da lista de substância proibidas e a coloca no rol de substâncias controladas.
                </p>
            </li>

            <li data-date="30/11/2015">
                <h2>2017 - Remédio de cannabis nas farmácias brasileiras</h2>
                <p>
                    Anvisa registra o primeiro medicamento à base de CBD e THC.
                </p>
            </li>

            <li data-date="15/12/2015">
                <h2>Hoje - Mais avanços estão por vir</h2>
                <p>
                    A cannabis segue fazendo história e parte da vida de milhões de pessoas pelo mundo. Nós continuaremos acompanhando tudo de pertinho e trazendo as novidades para você!
                </p>
            </li>

